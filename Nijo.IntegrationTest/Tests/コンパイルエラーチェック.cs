using Nijo.Core;
using Nijo.Util.DotnetEx;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Nijo.IntegrationTest.Tests {
    partial class 観点 {
        [UseDataPatterns]
        public async Task コンパイルエラーチェック(DataPattern pattern) {
            try {
                pattern.UpdateAutoGeneratedCode(TestProject.Current);

                // dotnet build, npm tsc による確認
                using var ct = new CancellationTokenSource();
                var ok = await TestProject.Current.CompilerCheck(ct.Token);

                Assert.That(ok, Is.True);

            } catch (Exception ex) {
                TestContext.Out.WriteLine("--- SCHEMA ---");
                TestContext.Out.WriteLine(TestProject.Current.BuildSchema().ToMermaidText());
                Assert.Fail(ex.Message);
            }
        }
    }
}
