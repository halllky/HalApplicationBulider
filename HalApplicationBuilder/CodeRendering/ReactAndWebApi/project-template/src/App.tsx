import React from 'react';
import { BrowserRouter, Route, Routes } from 'react-router-dom';
import { Panel, PanelGroup, PanelResizeHandle } from "react-resizable-panels";
import { AppContextProvider } from './__AutoGenerated/hooks/AppContext'
import { SideMenu } from './__AutoGenerated/components/SideMenu';
import { Dashboard } from './__AutoGenerated/components/Dashboard'
import { routes } from './__AutoGenerated';

function App() {

  return (
    <BrowserRouter>
      <AppContextProvider>
        <PanelGroup direction='horizontal'>
          <Panel defaultSize={20}>
            <SideMenu />
          </Panel>
          <PanelResizeHandle className='w-1' />
          <Panel>
            <Routes>
              <Route path='/' element={<Dashboard />} />
              {routes.map(route =>
                <Route key={route.url} path={route.url} element={route.el} />
              )}
              <Route path='*' element={<p>Not found.</p>} />
            </Routes>
          </Panel>
        </PanelGroup>
      </AppContextProvider>
    </BrowserRouter>
  );
}

export default App;
