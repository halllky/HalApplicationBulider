using Nijo.Util.CodeGenerating;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Nijo.Models.CommandModelFeatures {
    /// <summary>
    /// C#のクラスライブラリに作成される、
    /// <see cref="CommandModel"/> のパラメータクラスやメソッドの抽象部分が宣言されるファイル。
    /// </summary>
    internal class CoreLibraryFile : ISummarizedFile {

        private readonly List<(string Region, string Source)> _sourceCode = new();
        internal void Add(string regionName, string sourceCode) {
            _sourceCode.Add((regionName, sourceCode));
        }

        void ISummarizedFile.OnEndGenerating(CodeRenderingContext context) {
            context.CoreLibrary.AutoGeneratedDir(dir => {
                dir.Generate(new SourceFile {
                    FileName = "AutoGeneratedCommands.cs",
                    RenderContent = ctx => {
                        return $$"""
                            namespace {{ctx.Config.RootNamespace}};

                            {{_sourceCode.SelectTextTemplate(x => $$"""
                            #region {{x.Region}}
                            {{x.Source}}
                            #endregion {{x.Region}}

                            """)}}
                            """;
                    },
                });
            });
        }
    }
}
